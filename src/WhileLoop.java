public class WhileLoop {
    public static void main(String[] args) {

        /*
        * Notes!
        * > While merupakan versi sederhana dari For Loop
        * > Tanpa init statement dan post statement
        * */

        var counter = 1;

        while (counter <= 10){
            System.out.println("Perulangan " + counter);

            counter++;
        }

    }
}
